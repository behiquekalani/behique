# --- Voice Bible + Performance loading ---

def load_voice_bible_rules() -> str:
    """Load banned words and tone rules from the Voice Bible for prompt injection."""
    if not VOICE_BIBLE.exists():
        return ""
    try:
        content = VOICE_BIBLE.read_text(encoding="utf-8", errors="replace")
        # Extract the banned words section and tone rules
        sections = []
        capture = False
        for line in content.split("\n"):
            if "BANNED WORDS" in line or "TONE RULES" in line or "COPYWRITING RULES" in line:
                capture = True
            if capture:
                sections.append(line)
            # Stop after we have enough context
            if len(sections) > 80:
                break
        return "\n".join(sections) if sections else ""
    except Exception:
        return ""


def load_full_performance_insights() -> str:
    """Load detailed performance insights from analytics + insights files.

    Returns a formatted string for system prompt injection with:
    - Top-performing hooks
    - Topics that underperformed
    - Best content types and formats
    """
    insights_text = []

    # Load insights file first (generated by performance_tracker.py --insights)
    if INSIGHTS_FILE.exists():
        try:
            with open(INSIGHTS_FILE) as f:
                data = json.load(f)

            # Best hook styles
            hooks = data.get("best_hook_styles", [])
            if hooks:
                hook_list = []
                for h in hooks[:5]:
                    if isinstance(h, dict):
                        hook_list.append(f"{h.get('style', '?')} ({h.get('avg_engagement', 0)}% avg)")
                    else:
                        hook_list.append(str(h))
                insights_text.append(f"Previous high-performing hook styles: {', '.join(hook_list)}. Use similar patterns.")

            # Best topics
            topics = data.get("best_topic_keywords", [])
            if topics:
                good = [t["keyword"] for t in topics[:5] if isinstance(t, dict)]
                if good:
                    insights_text.append(f"High-engagement topic keywords: {', '.join(good)}. Weave these in when relevant.")

            # Best content types
            types = data.get("best_content_types", [])
            if types:
                type_list = []
                for t in types[:3]:
                    if isinstance(t, dict):
                        type_list.append(f"{t.get('type', '?')} ({t.get('avg_engagement', 0)}% avg)")
                if type_list:
                    insights_text.append(f"Best-performing formats: {', '.join(type_list)}.")

            # Recommendations
            recs = data.get("recommendations", [])
            if recs:
                insights_text.append("Performance recommendations:")
                for r in recs[:3]:
                    insights_text.append(f"  - {r}")

        except Exception:
            pass

    # Also check analytics directly for underperforming topics
    if ANALYTICS_FILE.exists():
        try:
            with open(ANALYTICS_FILE) as f:
                analytics = json.load(f)
            posts = analytics.get("posts", [])
            if len(posts) >= 5:
                # Find low performers
                sorted_posts = sorted(posts, key=lambda p: p.get("engagement_rate", 0))
                bottom = sorted_posts[:3]
                bottom_topics = [p.get("topic", "") for p in bottom if p.get("topic")]
                if bottom_topics:
                    insights_text.append(
                        f"Topics that underperformed: {', '.join(bottom_topics[:3])}. "
                        "Avoid these angles or find a fresh take."
                    )
        except Exception:
            pass

    return "\n".join(insights_text) if insights_text else ""


def _voice_check_output(script: dict) -> dict:
    """Run Voice Bible check on generated script text and auto-fix violations."""
    try:
        sys.path.insert(0, str(TOOLS_DIR))
        from voice_checker import fix_text
    except ImportError:
        return script

    # Check and fix all text fields
    text_fields = ["hook", "narration", "cta"]
    for key in text_fields:
        if key in script and isinstance(script[key], str):
            fixed, _ = fix_text(script[key])
            script[key] = fixed

    if "scenes" in script:
        for scene in script["scenes"]:
            for key in ["text", "overlay_text"]:
                if key in scene and isinstance(scene[key], str):
                    fixed, _ = fix_text(scene[key])
                    scene[key] = fixed

    return script


