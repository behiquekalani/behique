def query_ollama(system_prompt: str, user_prompt: str) -> str:
    """Send a prompt to Ollama and return the response text."""
    payload = {
        "model": OLLAMA_MODEL,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        "stream": False,
        "options": {
            "temperature": 0.7,
            "num_predict": 2048,
        },
    }

    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        f"{OLLAMA_URL}/api/chat",
        data=data,
        headers={"Content-Type": "application/json"},
    )

    try:
        with urllib.request.urlopen(req, timeout=120) as resp:
            result = json.loads(resp.read())
            return result.get("message", {}).get("content", "")
    except urllib.error.URLError as e:
        print(f"[ERROR] Cannot connect to Ollama at {OLLAMA_URL}")
        print(f"  Make sure Ollama is running: ollama serve")
        print(f"  Error: {e}")
        sys.exit(1)
    except Exception as e:
        print(f"[ERROR] Ollama request failed: {e}")
        sys.exit(1)


def parse_script_json(raw: str) -> dict:
    """Extract and parse JSON from LLM response, handling markdown fences."""
    # Strip markdown code fences if present
    cleaned = raw.strip()
    if cleaned.startswith("```"):
        # Remove opening fence (```json or ```)
        cleaned = re.sub(r"^```(?:json)?\s*\n?", "", cleaned)
        # Remove closing fence
        cleaned = re.sub(r"\n?```\s*$", "", cleaned)

    # Try to find JSON object in the text
    brace_start = cleaned.find("{")
    brace_end = cleaned.rfind("}")
    if brace_start >= 0 and brace_end > brace_start:
        cleaned = cleaned[brace_start:brace_end + 1]

    try:
        return json.loads(cleaned)
    except json.JSONDecodeError as e:
        print(f"[ERROR] Failed to parse LLM output as JSON: {e}")
        print(f"  Raw output (first 500 chars): {raw[:500]}")
        return {}


def load_performance_insights() -> str:
    """Load top-performing content insights from analytics to inform script writing."""
    if not ANALYTICS_FILE.exists():
        return ""

    try:
        with open(ANALYTICS_FILE) as f:
            data = json.load(f)

        posts = data.get("posts", [])
        if not posts:
            return ""

        # Find top posts by engagement
        scored = []
        for p in posts:
            eng = p.get("engagement_rate", 0)
            if eng > 0:
                scored.append(p)

        if not scored:
            return ""

        scored.sort(key=lambda x: x.get("engagement_rate", 0), reverse=True)
        top = scored[:5]

        lines = ["Top performing content patterns:"]
        for p in top:
            lines.append(f"- Topic: {p.get('topic', 'unknown')}, "
                         f"Type: {p.get('content_type', '?')}, "
                         f"Engagement: {p.get('engagement_rate', 0):.1f}%")
            if p.get("hook_style"):
                lines.append(f"  Hook style: {p['hook_style']}")

        return "\n".join(lines)
    except Exception:
        return ""


def load_generated_log() -> dict:
    """Load the log of previously generated scripts to avoid duplicates."""
    if GENERATED_LOG.exists():
        try:
            with open(GENERATED_LOG) as f:
                return json.load(f)
        except Exception:
            pass
    return {"generated": []}


def save_generated_log(log: dict):
    """Save the generated scripts log."""
    GENERATED_LOG.parent.mkdir(parents=True, exist_ok=True)
    with open(GENERATED_LOG, "w") as f:
        json.dump(log, f, indent=2, default=str)


def generate_script(topic: str, fmt: str = "hook", language: str = "en") -> dict:
    """Generate a video script using Ollama. Injects Voice Bible and performance data."""
    sys_prompt = _build_system_prompt(language)
    insights = load_performance_insights()
    user_prompt = _build_prompt(topic, fmt, language, insights)

    print(f"  [AI] Generating {FORMATS[fmt]['name']} script ({language})...")
    print(f"  [AI] Model: {OLLAMA_MODEL} @ {OLLAMA_URL}")

    raw = query_ollama(sys_prompt, user_prompt)
    script = parse_script_json(raw)

    if not script:
        print("  [!] Script generation failed. Check Ollama output.")
        return {}

    # Ensure required fields
    script.setdefault("language", language)
    script.setdefault("tags", [])
    script.setdefault("category", "content")

    # Add metadata
    script["metadata"] = {
        "format": fmt,
        "format_name": FORMATS[fmt]["name"],
        "target_duration": FORMATS[fmt]["duration"],
        "model": OLLAMA_MODEL,
        "generated": datetime.now().isoformat(),
        "topic_input": topic,
        "voice": "af_heart" if language == "en" else "af_heart",
        "speed": 0.95,
    }

    # Clean em dashes from all text fields
    for key in ["hook", "narration", "cta"]:
        if key in script and isinstance(script[key], str):
            script[key] = script[key].replace("\u2014", ".").replace("\u2013", ".")
            while ".." in script[key]:
                script[key] = script[key].replace("..", ".")

    if "scenes" in script:
        for scene in script["scenes"]:
            for key in ["text", "overlay_text", "image_prompt"]:
                if key in scene and isinstance(scene[key], str):
                    scene[key] = scene[key].replace("\u2014", ".").replace("\u2013", ".")
                    while ".." in scene[key]:
                        scene[key] = scene[key].replace("..", ".")

    # Voice Bible post-generation check: catch anything the LLM slipped through
    script = _voice_check_output(script)

    return script


def save_script(script: dict, output_dir: Path = None) -> Path:
    """Save a script to disk. Returns the file path."""
    if output_dir is None:
        output_dir = SCRIPTS_DIR

    output_dir.mkdir(parents=True, exist_ok=True)

    title = script.get("title", "untitled")
    # Sanitize title for filename
    safe_title = re.sub(r"[^a-z0-9_-]", "", title.lower().replace(" ", "-"))[:50]
    lang = script.get("language", "en")
    fmt = script.get("metadata", {}).get("format", "hook")
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")

    filename = f"script-{safe_title}-{fmt}-{lang}-{timestamp}.json"
    filepath = output_dir / filename

    with open(filepath, "w") as f:
        json.dump(script, f, indent=2, ensure_ascii=False)

    return filepath


def save_to_reel_pipeline(script: dict) -> Path:
    """Save script in reel-pipeline stories format for video production."""
    REEL_STORIES_DIR.mkdir(parents=True, exist_ok=True)

    title = script.get("title", "untitled")
    safe_title = re.sub(r"[^a-z0-9_-]", "", title.lower().replace(" ", "-"))[:50]

    filepath = REEL_STORIES_DIR / f"script-{safe_title}.json"

    # Convert to reel-pipeline format
    reel_data = {
        "title": title,
        "hook": script.get("hook", ""),
        "narration": script.get("narration", ""),
        "scenes": script.get("scenes", []),
        "tags": script.get("tags", []),
        "category": script.get("category", "content"),
        "language": script.get("language", "en"),
    }

    with open(filepath, "w") as f:
        json.dump(reel_data, f, indent=2, ensure_ascii=False)

    return filepath


def pick_from_ideas(count: int = 1) -> list:
    """Pick random topics from the content ideas database."""
    if not IDEAS_FILE.exists():
        print("[!] No content ideas found. Run: python3 content_extractor.py --scan")
        return []

    with open(IDEAS_FILE) as f:
        data = json.load(f)

    ideas = data.get("ideas", [])
    if not ideas:
        print("[!] Content ideas database is empty.")
        return []

    # Filter out already-generated topics
    log = load_generated_log()
    generated_ids = {g.get("idea_id") for g in log.get("generated", [])}
    available = [i for i in ideas if i.get("id") not in generated_ids]

    if not available:
        print("[!] All ideas have been used. Resetting tracker.")
        available = ideas

    count = min(count, len(available))
    return random.sample(available, count)


def main():
    parser = argparse.ArgumentParser(
        description="AI Script Generator. Uses Ollama to write video scripts. Copyright 2026 Behike."
    )
    parser.add_argument("--topic", type=str, help="Topic to write about")
    parser.add_argument("--format", dest="fmt", choices=list(FORMATS.keys()),
                        default="hook", help="Script format (default: hook)")
    parser.add_argument("--language", choices=["en", "es"], default="en",
                        help="Output language (default: en)")
    parser.add_argument("--both-languages", action="store_true",
                        help="Generate both EN and ES versions")
    parser.add_argument("--from-ideas", action="store_true",
                        help="Pick random topic from content-ideas.json")
    parser.add_argument("--count", type=int, default=1,
                        help="Number of scripts to generate (default: 1)")
    parser.add_argument("--output", type=str, help="Custom output directory")
    parser.add_argument("--reel", action="store_true",
                        help="Also save to reel-pipeline stories directory")
    parser.add_argument("--list-formats", action="store_true",
                        help="Show available script formats")
    parser.add_argument("--dry-run", action="store_true",
                        help="Show what would be generated without calling Ollama")

    args = parser.parse_args()

    if args.list_formats:
        print("\nAvailable script formats:\n")
        for key, fmt in FORMATS.items():
            print(f"  {key:10s}  {fmt['name']} ({fmt['duration']})")
            print(f"             {fmt['structure']}")
            print(f"             {fmt['description']}")
            print()
        return

    # Collect topics
    topics = []
    idea_map = {}

    if args.from_ideas:
        ideas = pick_from_ideas(args.count)
        for idea in ideas:
            t = idea.get("title", "")
            topics.append(t)
            idea_map[t] = idea
    elif args.topic:
        topics = [args.topic] * args.count
    else:
        parser.print_help()
        return

    if not topics:
        return

    output_dir = Path(args.output) if args.output else SCRIPTS_DIR
    log = load_generated_log()

    print()
    print("=" * 60)
    print("  BEHIKE SCRIPT WRITER")
    print("=" * 60)
    print(f"  Topics:   {len(topics)}")
    print(f"  Format:   {FORMATS[args.fmt]['name']} ({FORMATS[args.fmt]['duration']})")
    print(f"  Language: {'EN + ES' if args.both_languages else args.language.upper()}")
    print(f"  Model:    {OLLAMA_MODEL}")
    print()

    generated_files = []

    for i, topic in enumerate(topics, 1):
        print(f"[{i}/{len(topics)}] {topic}")

        if args.dry_run:
            print(f"  [DRY RUN] Would generate {args.fmt} script")
            if args.both_languages:
                print(f"  [DRY RUN] Would generate ES version too")
            continue

        languages = ["en", "es"] if args.both_languages else [args.language]

        for lang in languages:
            script = generate_script(topic, args.fmt, lang)
            if not script:
                continue

            filepath = save_script(script, output_dir)
            generated_files.append(filepath)
            print(f"  [SAVED] {filepath}")

            if args.reel:
                reel_path = save_to_reel_pipeline(script)
                print(f"  [REEL]  {reel_path}")

            # Log generation
            log_entry = {
                "topic": topic,
                "format": args.fmt,
                "language": lang,
                "file": str(filepath),
                "generated_at": datetime.now().isoformat(),
            }
            if topic in idea_map:
                log_entry["idea_id"] = idea_map[topic].get("id", "")
            log["generated"].append(log_entry)

        print()

    save_generated_log(log)

    print("=" * 60)
    print(f"  DONE. Generated {len(generated_files)} scripts.")
    if generated_files:
        print(f"  Output: {output_dir}/")
        if args.reel:
            print(f"  Reel pipeline: {REEL_STORIES_DIR}/")
    print("=" * 60)
    print()


if __name__ == "__main__":
    main()
